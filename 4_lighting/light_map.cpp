Camera camera(glm::vec3(0.0f, 0.0f, 3.0f));
float prevXPos = SCREEN_WIDTH / 2.0f;
float prevYPos = SCREEN_HEIGHT / 2.0f;
bool firstMouse = true;

// timing
float delta = 0.0f;
float prevFrame = 0.0f;

